package semaforos.ejer3Mod2;

public class Cesto {

    int mangas;
    int cuerpos;

    public Cesto(int mangas, int cuerpos) {
        this.mangas = mangas;
        this.cuerpos = cuerpos;
    }

    public int getMangas() {
        return mangas;
    }

    public void setMangas(int mangas) {
        this.mangas = mangas;
    }

    public int getCuerpos() {
        return cuerpos;
    }

    public void setCuerpos(int cuerpos) {
        this.cuerpos = cuerpos;
    }

}
